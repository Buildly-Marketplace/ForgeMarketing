    def send_daily_report_email(self, brand_key: str, email_content: Dict[str, str], dry_run: bool = False) -> bool:
        """Send daily report email for a specific brand"""
        try:
            brand_config = DAILY_EMAIL_CONFIG.get(brand_key, {})
            team_email = brand_config.get('team_email')
            cc_emails = brand_config.get('cc_emails', [])
            
            if not team_email:
                print(f"❌ No team email configured for {brand_key}")
                return False
            
            if dry_run:
                print(f"🧪 [DRY RUN] Would send daily report to {team_email} (CC: {', '.join(cc_emails)})")
                print(f"   Subject: {email_content['subject']}")
                return True
            
            # Create SMTP connection using proven Brevo config
            server = smtplib.SMTP(SMTP_CONFIG['smtp_server'], SMTP_CONFIG['smtp_port'])
            server.starttls()
            server.login(SMTP_CONFIG['username'], SMTP_CONFIG['password'])
            
            # Create message
            msg = MIMEMultipart('alternative')
            msg['From'] = f"{SMTP_CONFIG['from_name']} <{SMTP_CONFIG['from_email']}>"
            msg['To'] = team_email
            msg['Subject'] = email_content['subject']
            msg['Reply-To'] = SMTP_CONFIG['from_email']
            
            # Add CC emails
            if cc_emails:
                msg['Cc'] = ', '.join(cc_emails)
            
            # Add both text and HTML versions
            text_part = MIMEText(email_content['text_body'], 'plain')
            html_part = MIMEText(email_content['html_body'], 'html')
            
            msg.attach(text_part)
            msg.attach(html_part)
            
            # Send to all recipients
            all_recipients = [team_email] + cc_emails
            server.send_message(msg, to_addrs=all_recipients)
            server.quit()
            
            print(f"✅ Daily report sent to {team_email} (CC: {', '.join(cc_emails)})")
            return True
            
        except Exception as e:
            print(f"❌ Failed to send daily report for {brand_key}: {e}")
            return False
    
    def send_all_daily_reports(self, dry_run: bool = False) -> Dict[str, bool]:
        """Send daily reports for all brands"""
        results = {}
        
        print(f"🚀 Sending daily analytics reports for all brands...")
        if dry_run:
            print("🧪 DRY RUN MODE - No emails will be sent")
        
        for brand_key in DAILY_EMAIL_CONFIG.keys():
            try:
                print(f"\n📊 Processing {brand_key.title()}...")
                
                # Get analytics data for this brand
                analytics_data = get_analytics_for_dashboard(brand_key)
                
                # Generate email content
                email_content = self.generate_daily_email_content(brand_key, analytics_data)
                
                # Send email
                success = self.send_daily_report_email(brand_key, email_content, dry_run)
                results[brand_key] = success
                
            except Exception as e:
                print(f"❌ Error processing {brand_key}: {e}")
                results[brand_key] = False
        
        # Summary
        successful = sum(results.values())
        total = len(results)
        
        print(f"\n📈 Daily Reports Summary:")
        print(f"   ✅ Successful: {successful}")
        print(f"   ❌ Failed: {total - successful}")
        print(f"   📊 Total: {total}")
        
        return results
    
