def _load_daily_email_config() -> Dict[str, Dict[str, Any]]:
    """Load brand email configurations from database"""
    config = {}
    brands = get_all_brands(active_only=True)
    
    for brand in brands:
        brand_data = get_brand_details(brand)
        if brand_data:
            config[brand] = {
                'name': brand_data.get('display_name', brand.title()),
                'website': brand_data.get('website_url', f'https://{brand}.io'),
                'team_email': os.getenv(f'{brand.upper()}_TEAM_EMAIL', f'team@{brand}.io'),
                'cc_emails': [os.getenv('ADMIN_EMAIL', 'admin@buildly.io')],
                'notification_subject': f"{brand_data.get('display_name', brand.title())} Daily Analytics Report"
            }
    
    return config

