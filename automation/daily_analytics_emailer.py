def main():
    """Test the daily analytics email system"""
    import argparse
    
    parser = argparse.ArgumentParser(description='Send daily analytics reports')
    parser.add_argument('--dry-run', action='store_true', help='Test run without sending emails')
    parser.add_argument('--brand', type=str, help='Send report for specific brand only')
    parser.add_argument('--summary-only', action='store_true', help='Send only multi-brand summary')
    
    args = parser.parse_args()
    
    emailer = DailyAnalyticsEmailer()
    
    if args.summary_only:
        # Send only the multi-brand summary
        emailer.send_multi_brand_summary(dry_run=args.dry_run)
    elif args.brand:
        # Send report for specific brand
        analytics_data = get_analytics_for_dashboard(args.brand)
        email_content = emailer.generate_daily_email_content(args.brand, analytics_data)
        emailer.send_daily_report_email(args.brand, email_content, dry_run=args.dry_run)
    else:
        # Send all brand reports + summary
        emailer.send_all_daily_reports(dry_run=args.dry_run)
        print("\n📧 Sending multi-brand summary...")
        emailer.send_multi_brand_summary(dry_run=args.dry_run)

if __name__ == '__main__':
    main()